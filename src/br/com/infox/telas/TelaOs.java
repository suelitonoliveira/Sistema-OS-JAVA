    private void pesquisar_cliente() {
        // pesquisando cliente e sua OS correspondente
        String sql = "select tbos.os as OS , tbclientes.nomecli as Nome, "
                + "tbclientes.fonecli as Fone, tbclientes.idcli as id"
                + " from tbclientes, tbos where tbclientes.nomecli like ? order by os desc";

        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtCliPesquisar.getText() + "%");
            rs = pst.executeQuery();
            tblClientes.setModel(DbUtils.resultSetToTableModel(rs));
             
              
                 

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

